fun main(){
    //break->when a certain condiion arrives then it goes out of a loop
    for (i in 1..10){
        if (i==3)
            break
        print(i)
    }
    //continue->when we want to pass any condition then
    for (i in 1..10){
        if (i==3)
            continue
        print(i)
    }

    // return is used when we return some value from function
}